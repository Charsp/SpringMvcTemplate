package com.swz.mysql.gps.model;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "usermain_veh")
public class Usermain_veh {
	@Id
	private Integer UserMainVehID;
	private Integer UserID;
	private Integer VehicleID;

	public void setUserMainVehID(Integer UserMainVehID) {
		this.UserMainVehID = UserMainVehID;
	}

	public Integer getUserMainVehID() {
		return UserMainVehID;
	}

	public void setUserID(Integer UserID) {
		this.UserID = UserID;
	}

	public Integer getUserID() {
		return UserID;
	}

	public void setVehicleID(Integer VehicleID) {
		this.VehicleID = VehicleID;
	}

	public Integer getVehicleID() {
		return VehicleID;
	}
}
